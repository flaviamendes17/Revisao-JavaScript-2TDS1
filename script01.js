// Se a altura da pessoa for maior ou igual a altura minima e menor que a altura minima: permitido, senão, não permitido. 

let alturaPessoa = 1.70;
let alturaMinima = 1.40;
let alturaMaxima = 2.00;
if (alturaPessoa >= alturaMinima &&  alturaPessoa < alturaMaxima) {
    console.log("Permitido!");
} else {
    console.log("Não permitido");
}


